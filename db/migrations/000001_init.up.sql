-- =================================================================================
-- 1. TABEL MASTER (Tanpa Dependencies)
-- =================================================================================

CREATE TABLE IF NOT EXISTS COMPANY (
    ID_COMPANY SERIAL PRIMARY KEY,
    NAMA VARCHAR(100) NOT NULL,
    ALAMAT TEXT NOT NULL DEFAULT '',
    EMAIL VARCHAR(100) NOT NULL DEFAULT '',
    NO_TELP VARCHAR(50) NOT NULL DEFAULT '',
    ABOUT TEXT NOT NULL DEFAULT '',
    LOGO TEXT NOT NULL DEFAULT '',
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE TABLE IF NOT EXISTS LOG_AKTIVITAS (
    ID_LOG SERIAL PRIMARY KEY,
    AKSI VARCHAR(255) NOT NULL,
    WAKTU TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE TABLE IF NOT EXISTS HAK_AKSES (
    ID_HAK_AKSES SERIAL PRIMARY KEY,
    NAMA_HALAMAN VARCHAR(100) NOT NULL,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE TABLE IF NOT EXISTS MITRA (
    ID_MITRA SERIAL PRIMARY KEY,
    NAMA_PERUSAHAAN VARCHAR(150) NOT NULL,
    TIPE_PERUSAHAAN VARCHAR(100) NOT NULL,
    EMAIL VARCHAR(100) NOT NULL DEFAULT '',
    NO_TELP VARCHAR(50) NOT NULL DEFAULT '',
    ALAMAT TEXT NOT NULL DEFAULT '',
    KOTA VARCHAR(100) NOT NULL DEFAULT '',
    KODE_POS VARCHAR(20) NOT NULL DEFAULT '',
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE TABLE IF NOT EXISTS JENIS_BARANG (
    ID_JENIS_BARANG SERIAL PRIMARY KEY,
    NAMA_JENIS_BARANG VARCHAR(100) NOT NULL,
    KODE VARCHAR(100) NOT NULL UNIQUE, -- Tambahan UNIQUE
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE TABLE IF NOT EXISTS DEPARTEMEN (
    ID_DEPARTEMEN SERIAL PRIMARY KEY,
    NAMA_DEPARTEMEN VARCHAR(100) NOT NULL,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE TABLE IF NOT EXISTS SURAT_JALAN_INTERNAL (
    ID_SURAT_JALAN_INTERNAL SERIAL PRIMARY KEY,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

-- =================================================================================
-- 2. TABEL LEVEL 2 (User & Master Lanjutan)
-- =================================================================================

-- Penamaan tabel USER diganti menjadi USERS untuk menghindari Reserved Keyword SQL
CREATE TABLE IF NOT EXISTS USERS (
    ID_USER SERIAL PRIMARY KEY,
    USERNAME VARCHAR(100) NOT NULL UNIQUE,
    PASSWORD VARCHAR(255) NOT NULL,
    IS_MANAGER BOOLEAN NOT NULL DEFAULT FALSE,
    ID_DEPARTEMEN INT,
    ID_MITRA INT,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    FOREIGN KEY (ID_DEPARTEMEN) REFERENCES DEPARTEMEN(ID_DEPARTEMEN) ON DELETE SET NULL,
    FOREIGN KEY (ID_MITRA) REFERENCES MITRA(ID_MITRA) ON DELETE SET NULL
);

CREATE TABLE IF NOT EXISTS LOG_AKTIVITAS_DETAIL (
    ID_LOG_DETAIL SERIAL PRIMARY KEY,
    NAMA VARCHAR(100) NOT NULL,
    TABLE_NAME VARCHAR(100) NOT NULL DEFAULT '', -- Mengganti keyword "TABLE"
    DESKRIPSI TEXT NOT NULL DEFAULT '',
    ID_LOG INT NOT NULL,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    FOREIGN KEY (ID_LOG) REFERENCES LOG_AKTIVITAS(ID_LOG) ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS USER_AKSES (
    ID_USER INT NOT NULL,
    ID_HAK_AKSES INT NOT NULL,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    PRIMARY KEY (ID_USER, ID_HAK_AKSES),
    FOREIGN KEY (ID_USER) REFERENCES USERS(ID_USER) ON DELETE CASCADE,
    FOREIGN KEY (ID_HAK_AKSES) REFERENCES HAK_AKSES(ID_HAK_AKSES) ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS BARANG (
    ID_BARANG SERIAL PRIMARY KEY,
    NAMA_BARANG VARCHAR(150) NOT NULL,
    KODE VARCHAR(100) NOT NULL UNIQUE, -- Tambahan UNIQUE
    ID_JENIS_BARANG INT NOT NULL,
    ID_MITRA INT NOT NULL,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    FOREIGN KEY (ID_JENIS_BARANG) REFERENCES JENIS_BARANG(ID_JENIS_BARANG),
    FOREIGN KEY (ID_MITRA) REFERENCES MITRA(ID_MITRA)
);

CREATE TABLE IF NOT EXISTS PO_CLIENT (
    ID_PO_CLIENT SERIAL PRIMARY KEY,
    PO_NUMBER VARCHAR(100) NOT NULL UNIQUE, -- Tambahan UNIQUE
    TANGGAL DATE NOT NULL,
    SEASON VARCHAR(100) NOT NULL DEFAULT '',
    DELIVERY DATE NOT NULL,
    PAYMENT_TERM VARCHAR(100) NOT NULL DEFAULT '',
    FILE TEXT NOT NULL DEFAULT '',
    ID_MITRA INT NOT NULL,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    FOREIGN KEY (ID_MITRA) REFERENCES MITRA(ID_MITRA)
);

-- =================================================================================
-- 3. TABEL LEVEL 3 (Transaksi PO & Penanggung Jawab)
-- =================================================================================

CREATE TABLE IF NOT EXISTS PO_CLIENT_ITEM (
    ID_PO_CLIENT_ITEM SERIAL PRIMARY KEY,
    ID_PO_CLIENT INT NOT NULL,
    STYLE VARCHAR(100) NOT NULL,
    COLOUR VARCHAR(100) NOT NULL,
    DESCRIPTION TEXT NOT NULL DEFAULT '', -- Mengganti keyword "DESC"
    QTY INT NOT NULL DEFAULT 0,
    PRICE DECIMAL(15,2) NOT NULL DEFAULT 0.00, -- Mencegah Overflow harga
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    FOREIGN KEY (ID_PO_CLIENT) REFERENCES PO_CLIENT(ID_PO_CLIENT) ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS PENANGGUNG_JAWAB (
    ID_PENANGGUNG_JAWAB SERIAL PRIMARY KEY,
    NAMA VARCHAR(100) NOT NULL,
    NO_TELP VARCHAR(50) NOT NULL DEFAULT '',
    EMAIL VARCHAR(100) NOT NULL DEFAULT '',
    ID_PO_CLIENT INT NOT NULL,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    FOREIGN KEY (ID_PO_CLIENT) REFERENCES PO_CLIENT(ID_PO_CLIENT) ON DELETE CASCADE
);

-- =================================================================================
-- 4. TABEL LEVEL 4 (Work Order Produksi & PR Internal)
-- =================================================================================

-- Perbaikan konsistensi snake_case pada kolom ID_PO_CLIENT_ITEM
CREATE TABLE IF NOT EXISTS WORK_ORDER (
    ID_WO SERIAL PRIMARY KEY,
    BUYER VARCHAR(100) NOT NULL,
    MODEL VARCHAR(100) NOT NULL,
    QTY INT NOT NULL DEFAULT 0,
    FOB_CMT BOOLEAN NOT NULL DEFAULT FALSE,
    DELIVERY DATE NOT NULL,
    ID_PO_CLIENT_ITEM INT NOT NULL,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    FOREIGN KEY (ID_PO_CLIENT_ITEM) REFERENCES PO_CLIENT_ITEM(ID_PO_CLIENT_ITEM)
);

CREATE TABLE IF NOT EXISTS PR_INTERNAL (
    ID_PR_INTERNAL SERIAL PRIMARY KEY,
    TANGGAL DATE NOT NULL,
    NAMA VARCHAR(100) NOT NULL,
    DEPARTEMEN VARCHAR(100) NOT NULL,
    VENDOR_NAME VARCHAR(150) NOT NULL,
    VENDOR_ADDRESS TEXT NOT NULL DEFAULT '',
    VENDOR_TELP VARCHAR(50) NOT NULL DEFAULT '',
    PROJEK VARCHAR(100) NOT NULL,
    ID_WO INT NOT NULL,
    ID_USER INT NOT NULL,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    FOREIGN KEY (ID_WO) REFERENCES WORK_ORDER(ID_WO),
    FOREIGN KEY (ID_USER) REFERENCES USERS(ID_USER)
);

