CREATE TABLE IF NOT EXISTS PO_INTERNAL (
    ID_PO_INTERNAL SERIAL PRIMARY KEY,
    TANGGAL DATE NOT NULL,
    NAMA_PO VARCHAR(100) NOT NULL,
