CREATE TABLE IF NOT EXISTS PACKING_LIST (
    ID_PACKING_LIST SERIAL PRIMARY KEY,
    TOTAL_GARMENT_PER_BOX INT NOT NULL DEFAULT 0,
    TOTAL_REJECT INT NOT NULL DEFAULT 0,
